        },
        goTo(page) {
          if (this.$route.name !== page) {
            this.setAnimate('up')
            this.setPlug(true)
            setTimeout(() => {
              this.setAnimate('dissolve')
              this.$router.push(this.localePath(page))
            }, 1000);
          }
        },
